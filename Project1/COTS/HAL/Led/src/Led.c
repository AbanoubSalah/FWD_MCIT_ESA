Led_Status Led_On(uint32 channel)
{
	if(43 < channel)
	{
		return Led_NOT_OK;
	}
	
	DIO_WriteChannel(channel, STD_HIGH);
	
	return Led_OK;
}
Led_Status Led_Off(uint32 channel)
{
	if(43 < channel)
	{
		return Led_NOT_OK;
	}
	
	DIO_WriteChannel(channel, STD_LOW);
	
	return Led_OK;
}

