Button_Status Button_isPressed(Dio_ChannelType channel)
{
	Button_Status buttonState = Button_Off;
	
	if(DIO_LEVEL_HIGH == Dio_ReadChannel(channel))
	{
		buttonState = Button_On;
	}
	
	return buttonState;
}

